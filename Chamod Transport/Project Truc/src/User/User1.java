    private void b7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b7ActionPerformed
        // TODO add your handling code here:
          if(table3.getSelectedRow()==-1){
           JOptionPane.showMessageDialog(rootPane, "please select an order from the table");
           return;
         } 
      
      String stage="";
      if(r13.isSelected())stage="1";
      if(r14.isSelected())stage="2";
      
      int row =table3.getSelectedRow();
      int outSource=Integer.parseInt(t18.getText());
      int count=Integer.parseInt(jTextField13.getText());
      int remain=Integer.parseInt(t16.getText());
      int procCont=Integer.parseInt(t17.getText());

      String date=day1.getText();
      String orderID=String.valueOf(table3.getValueAt(row, 0));
      if(outSource+count > remain && che3.isSelected()){
           JOptionPane.showMessageDialog(rootPane, "the max no of outsourceble trucks is :"+String.valueOf(remain-count));
           return;
         }
      String[] driverIDs=new String[count];
    
      for(int i=0;i<count;i++){
          String[] tmp=jList4drivers.get(i).toString().split("-");
          driverIDs[i]=sql.getSingleResult("select  driverID from driverdetails where TP='"+tmp[1]+"';");
      }
      
      sql.setDriverOrder_array(driverIDs,orderID,stage,date);
      
      
       refreshJTable2();             
      
      if(che3.isSelected()){
            
            String[] vals={orderID
                           ,comboBox3List.get(combo3.getSelectedIndex())
                           
                           ,String.valueOf(outSource)
                           ,"0"
                           ,"NO"
                           ,date
                           ,date
                           ,stage
                           ,"no"
                           }; 
            sql.addNewRow("transportsonduty", vals);
            sql.queryPass("update orders set procContainers='"+String.valueOf(procCont+count+outSource)+"' where orderID='"+orderID+"'","update");
      }
      else{
      
             sql.queryPass("update orders set procContainers='"+String.valueOf(procCont+count)+"' where orderID='"+orderID+"'","update");

      }
    }//GEN-LAST:event_b7ActionPerformed

