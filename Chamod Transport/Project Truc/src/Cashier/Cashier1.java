 private void refreshComboBox1(){
     list1.clear();
     combo1.removeAllItems();
     
     ResultSet shipIDs=null,transIDs=null;
    if(r7.isSelected()){
        shipIDs=sql.getResultSet("SELECT name,lineID FROM shippinglines where  lineID in(select shipLineID from orders where isaTransporter='no' and paymentDone='yes') ;");
        transIDs=sql.getResultSet("SELECT name,transID FROM transporterdetails where  transID in(select shipLineID from orders where isaTransporter='yes' and paymentDone='no');");

    }  
    else if(r8.isSelected()){
        shipIDs=sql.getResultSet("SELECT name,lineID FROM shippinglines where  lineID in(select shipLineID from orders where isaTransporter='no' and paymentDone='no') ;");
        transIDs=sql.getResultSet("SELECT name,transID FROM transporterdetails where  transID in(select shipLineID from orders where isaTransporter='yes' and paymentDone='no');");
    
          
    }       
     try{
         int hasNext=0;
         
         
         while(shipIDs.next()){
             hasNext=1;
             combo1.addItem(shipIDs.getString("name"));
             list1.add(shipIDs.getString("lineID"));
         }
        while(transIDs.next()){
             hasNext=1;
             combo1.addItem(transIDs.getString("name"));
             list1.add(transIDs.getString("transID"));
         }
         
         if(hasNext==0){
             JOptionPane.showMessageDialog(rootPane,"No orders to display");
            
         }
     
     }
        catch(Exception e){e.printStackTrace();}
 }
 
