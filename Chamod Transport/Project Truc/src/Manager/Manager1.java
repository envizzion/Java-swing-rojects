 private void refreshComboBox1(){
     ResultSet rs1=sql.getResultSet("SELECT truckID FROM truckdetails having truckID not in(select truckID from drivertruck);");
       
     try{
         int hasNext=0;
         while(rs1.next()){
             hasNext=1;
             combo2.addItem(rs1.getString("truckID"));
         }
         if(hasNext==0){
             JOptionPane.showMessageDialog(rootPane,"No Free trucks are available");
             che1.setEnabled(false);
         }
     
     }
        catch(Exception e){e.printStackTrace();}
 }
 
