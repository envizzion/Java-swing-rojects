    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // Create child Accounts

        FormValidate ff1=new FormValidate();
        JTextField f4[]={jTextField54,jTextField48,jTextField53,jTextField55,jTextField56,jTextField57,age};
        if(!ff1.FieldsAreEmpty(f4)){

            try {
                // Create Accounts and get values from members details
                Connection c = Dbconnection.mycon();
                Statement s = c.createStatement();
                GetterSetter gs1=new GetterSetter();

                double dptamnt =0.00;
                double wthamnt = 0.00;
                double total = 0.00;

                String type = jComboBox3.getSelectedItem().toString();
                int x = JOptionPane.showConfirmDialog(rootPane, "Do you want to save this Record ? ");

                if (x == 0){
                    boolean isaGuardian=false;

                    if(gs1.isaMember(type))JOptionPane.showMessageDialog(rootPane, "The NIC holder is a member so use member area for further proceedings");

                    else if(type.equals("Kekulu") && (gs1.hasChildAccnt(jTextField54.getText(),jTextField48.getText())))JOptionPane.showMessageDialog(rootPane, "The NIC holder already has a Kekulu account for the same chid");
                    else if(type.equals("Sisuraka") && (gs1.hasChildAccnt(jTextField54.getText(),jTextField48.getText())))JOptionPane.showMessageDialog(rootPane, "The NIC holder already has a Sisuraka account for the same child");

                    else{

                        if(gs1.hasAccnt("GUR", jTextField54.getText()))isaGuardian=true;

                        if(type.equals("Kekulu") ){
                            String addr=gs1.concatAddr(jTextField55,jTextField56,jTextField57);
                            String accNo=gs1.createAccNo("KE");

                            s.executeUpdate("Insert into child(AccNo,StartDate,DepositAmount,WithdrawAmount, Total,Type,childName,gNIC) values ('"+accNo+"','"+dateChooserCombo2.getText()+"','"+dptamnt+"','"+wthamnt+"','"+total+"','"+"KE"+"','"+jTextField48.getText()+"','"+jTextField54.getText()+"v') ");
                            JOptionPane.showMessageDialog(rootPane, "record has been saved into child successfully");
                            s.executeUpdate("Insert into child_info(Name,gNIC,Age,DoB,Address) values('"+jTextField48.getText()+"','"+jTextField54.getText()+"v','"+age.getText()+"','"+dateChooserCombo4.getText()+"','"+addr+"')");
                            JOptionPane.showMessageDialog(rootPane, "record has been saved into child_info successfully");

                            if(!isaGuardian){
                                s.executeUpdate("Insert into guardian(NIC,Name,Address) values('"+jTextField54.getText()+"v','"+jTextField53.getText()+"','"+addr+"')");
                                JOptionPane.showMessageDialog(rootPane, "record has been saved into Guardian successfully");
                            }
                            else JOptionPane.showMessageDialog(rootPane, "the guardian is already registered : No updates to guardian table");
                        }

                        else if(type.equals("Sisuraka")){

                            String addr=gs1.concatAddr(jTextField55,jTextField56,jTextField57);
                            s.executeUpdate("Insert into child(AccNo,StartDate,DepositAmount,WithdrawAmount, Total,Type,childName,gNIC) values ('"+gs1.createAccNo("SI")+"','"+dateChooserCombo2.getText()+"','"+dptamnt+"','"+wthamnt+"','"+total+"','"+"SI"+"','"+jTextField48.getText()+"','"+jTextField54.getText()+"v') ");
                            JOptionPane.showMessageDialog(rootPane, "record has been saved into child successfully");
                            s.executeUpdate("Insert into child_info(Name,gNIC,DoB,Address) values('"+jTextField48.getText()+"','"+jTextField54.getText()+"v','"+dateChooserCombo4.getText()+"','"+addr+"')");

                            JOptionPane.showMessageDialog(rootPane, "record has been saved into child_info successfully");

                            if(!isaGuardian){
                                s.executeUpdate("Insert into guardian(NIC,Name,Address) values('"+jTextField54.getText()+"v','"+jTextField53.getText()+"','"+addr+"')");
                                JOptionPane.showMessageDialog(rootPane, "record has been saved into Guardian successfully");
                            }
                            else JOptionPane.showMessageDialog(rootPane, "the guardian is already registered : No updates to guardian table");
                        }

                    }
                }
                else if(x == 1){

                }
            } catch (Exception ex) {
                // ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton7ActionPerformed

