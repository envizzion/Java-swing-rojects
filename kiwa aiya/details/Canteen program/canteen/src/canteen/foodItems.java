    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try{
             Connection c = new BSDB().con();
               int row=jTable1.getSelectedRow();
           String Table_Click=(jTable1.getModel().getValueAt(row,0).toString());
              String Sql="Select * from food_item where Food_ID='"+ Table_Click+"'";
              
           pst=c.prepareStatement(Sql);
            rs=pst.executeQuery(Sql);
           if(rs.next()){
               add1=rs.getString("Food_ID");
                jTextField1.setText(add1);
                 String add2=rs.getString("Food_name");
                jTextField2.setText(add2);
                String add3=rs.getString("unit_Price");
                jTextField3.setText(add3);
                String add4 =rs.getString("stock_amount");
                jTextField4.setText(add4);
                
            }
              
              
              
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

