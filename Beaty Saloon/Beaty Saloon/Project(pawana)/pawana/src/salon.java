    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if(f1.FieldsAreEmpty(jPanel2))return;
        String id=jTextField1.getText();
        String dType=jTextField2.getText();
        String matType=jTextField5.getText();
       String details=jTextArea1.getText();
        String price=jTextField14.getText();

        String[][] arr={{"dress_id",id},
                         {"dress_type",dType},
                         {"mat_type",matType},
                         {"details",details},
                         {"price",price}
                         
                        };
        
        
        if(jRadioButton1.isSelected() ){
            if(sql.idChecker("dress_details", "dress_id", id)){
              sql.editRow("dress_details", "where dress_id='"+id+"';", arr);
               refrJtable1();
            }
            else sql.printMeassage("entered id doesnt exist");
        }
        
        else {
           if(!sql.idChecker("dress_details", "dress_id", id)){
              String[] ar={dType,matType,details,price};
              sql.addNewRowNoID("dress_details",  ar);
               refrJtable1();
            }
           else sql.printMeassage("entered id already exists");
        
        
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

