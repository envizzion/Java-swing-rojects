    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        if(f1.FieldsAreEmpty(jPanel3))return;
        String name=jTextField15.getText();
        String price=jTextField16.getText();
        String desc=jTextArea2.getText();
        String[][] arr={{"Description",desc},
                         {"price",price}
                        };
        
        
        if(jRadioButton3.isSelected() ){
            if(sql.idChecker("jewellery", "name", name)){
              sql.editRow("jewellery", "where name='"+name+"';", arr);
               refrJtable2();
            }
            else sql.printMeassage("entered name doesnt exist");
        }
        
        else {
           if(!sql.idChecker("jewellery", "name", name)){
              String[] ar={name,desc,price};
              sql.addNewRow("jewellery",  ar);
               refrJtable2();
            }
           else sql.printMeassage("entered name already exists");
        
        
        }
        
    }//GEN-LAST:event_jButton9ActionPerformed

