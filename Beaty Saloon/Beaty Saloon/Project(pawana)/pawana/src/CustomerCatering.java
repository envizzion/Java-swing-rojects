    private void jTextField7KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyReleased
        if(jTextField3.getText().isEmpty()){
            sql.printMeassage("please fill the customer name");
            DefaultTableModel tbl=new DefaultTableModel();
            jTable2.setModel(tbl);
            return;
        }

        if(!sql.idChecker("customer","cusID", jTextField3.getText())){
            sql.printMeassage("Invalid customer ID");
            DefaultTableModel tbl=new DefaultTableModel();
            jTable2.setModel(tbl);
            jTextField3.setText("");
            return;

        }
        String type=jComboBox6.getSelectedItem().toString();
        if(!jTextField7.getText().isEmpty()){

            sql.setTableModel(jTable2, "select * from catering_services where Service_Type='"+type+"' and Name like '"+jTextField7.getText()+"%' ;");

        }
        else {
            sql.setTableModel(jTable2, "select * from catering_services where Service_Type='"+type+"' ;");

        }
    }//GEN-LAST:event_jTextField7KeyReleased

