    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String gender="";
        if(jRadioButton1.isSelected())gender="male";
        else gender="female";
        
        String[] arr={jTextField3.getText(),
                     jTextField1.getText(),
                     jTextField4.getText(),
                     jTextField5.getText(),
                     jComboBox2.getSelectedItem().toString(),
                     dateChooserCombo1.getText(),
                     gender
                     };
        
        if( sql.idChecker("employee", "NIC",jTextField3.getText() )){
              sql.printMeassage("The NIC already exists further proceedings not possible");
              return;
          }
          
          if(!f1.FieldsAreEmpty(jPanel1)){
            
              sql.addNewRow("employee",arr );
               f1.clearPanelTextFields(jPanel1);
              sql.printMeassage("  new user added successfully");
              

          }       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
           if( !sql.idChecker("employee", "NIC",jTextField3.getText() )){
              sql.printMeassage("The entered NIC doesnt exist");
              return;
          }
           sql.deleteByID("employee", "NIC",jTextField3.getText() );
           sql.printMeassage("Deletion success");
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         String gender="";
        if(jRadioButton1.isSelected())gender="male";
        else gender="female";
        
        String[][] arr={  { "NIC",jTextField3.getText()},
                          { "Name",jTextField1.getText()},
                          { "Address",jTextField4.getText()},
                          { "Contact_no",jTextField5.getText()},
                          { "Section",jComboBox2.getSelectedItem().toString()},
                          { "Joined_date",dateChooserCombo1.getText()},
                          { "Gender",gender}
                        };
        
        if( !sql.idChecker("employee", "NIC",jTextField3.getText() )){
              sql.printMeassage("The NIC doesnt exist so further editing is not possible");
              return;
          }
          
          if(!f1.FieldsAreEmpty(jPanel1)){
            
              sql.editRow("employee", "where NIC='"+jTextField3.getText()+"';", arr);
               f1.clearPanelTextFields(jPanel1);
              sql.printMeassage("  Edit success");
              

          }               
    }//GEN-LAST:event_jButton4ActionPerformed

