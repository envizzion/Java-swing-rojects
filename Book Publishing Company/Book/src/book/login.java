    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try {

            Statement st1 = Db.getconneConnection().createStatement();
            ResultSet rs = st1.executeQuery("SELECT * FROM login WHERE username='"+txt_username.getText()+"'");
            while(rs.next()){
                us = rs.getString("username");
                ps = rs.getString("password");
            }
            String a = new String(txt_password.getPassword());
            if(us.equals(txt_username.getText())){
                if(ps.equals(a)){
                    main m = new main();
                    m.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this,"Password Incorrect..!","Error",2);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

